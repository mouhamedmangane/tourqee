package com.boutique.dto;

public class RoleDTO {
	
	private long idRole;
	
	private String nom;

	public RoleDTO() {
		super();
	}

	public long getIdRole() {
		return idRole;
	}

	public void setIdRole(long idRole) {
		this.idRole = idRole;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}
	
	
}
