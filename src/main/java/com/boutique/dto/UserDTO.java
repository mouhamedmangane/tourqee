package com.boutique.dto;

public class UserDTO {
	
	private long idUser;
	private String nom;
	public UserDTO() {
		super();
	}
	public long getIdUser() {
		return idUser;
	}
	public void setIdUser(long idUser) {
		this.idUser = idUser;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	
	

}
